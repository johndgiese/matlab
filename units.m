%% DEFINE PHYSICAL CONSTANTS IN KGS UNITS

h = 6.626068e-34; % Planck's constant [J*s] = [kg*m^2/s]
hbar = h/2/pi; % Reduced Planck's constant [J*s/rad]
k = 1.3806503e-23; % Boltzmann constant [J/K] = [m^2*kg/s^2*K]
c = 299792458; % Speed of light in a vacuum [m/s]
e = 1.60217646e-19; % Elementary charge [C]
m_e = 9.10938188e-31; % Electron mass [kg]
m_p = 1.67262158e-27; % Proton mass [kg]
amu = 1.66053886e-27; % Atomic mass unit [kg]